using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Exercise_p36_Csharp_Course
{
    class NumOps
    {
        public static double AddFive (double num)
        {
            double sum = num + 5;
            return sum;
        }

        public static double SubtractFive(double num)
        {
            double sum = num - 5;
            return sum;
        }

        public static double MultiplyFive(double num)
        {
            double sum = num * 5;
            return sum;
        }
    }
}
